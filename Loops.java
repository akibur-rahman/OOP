public class Loops {
    public static void main(String[] args) {
        int i = 0;

        // while loop
        while (i < 10) {
            System.out.println(i);
            i++;
        }
        // for loop
        for (i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // do-while loop
        do {
            System.out.println(i);
            i++;
        } while (i < 10);

    }
}
