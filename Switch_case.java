public class Switch_case {
    public static void main(String[] args) {
        int choice = 8;
        switch (choice) {
            case 0:
                System.out.println("choice is 0");
                break;
            case 1:
                System.out.println("choice is 1");
                break;
            case 2:
                System.out.println("choice is 2");
                break;
            default:
                System.out.println("invalid choice");
                break;
        }
    }
}
